ALTER TABLE requests ADD FOREIGN KEY (requester_id) REFERENCES users(user_id);

ALTER TABLE events_compilations ADD FOREIGN KEY (event_id) REFERENCES events(id);
ALTER TABLE events_compilations ADD FOREIGN KEY (compilation_id) REFERENCES compilations(compilation_id);

